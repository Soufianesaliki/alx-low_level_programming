#include "3-calc.h"

/**
 * op_add - adds a to b
 * @a: int
 * @b: int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substracte a from b
 * @a: int
 * @b: int
 * Return: sub of a and b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply a and b
 * @a: int
 * @b: int
 * Return: mul of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a and b
 * @a: int
 * @b: int
 * Return: a over b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of  a and b
 * @a: int
 * @b: int
 * Return: mod of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
